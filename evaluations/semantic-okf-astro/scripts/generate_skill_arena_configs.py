class Dumper(yaml.SafeDumper):
    """Indent sequence items so Skill Arena's design validator can parse prompts."""

    def increase_indent(self, flow: bool = False, indentless: bool = False) -> Any:
        return super().increase_indent(flow, False)


class ConfigError(RuntimeError):
    """Describe an invalid accepted build or config-generation input."""


def canonical_json(value: Any) -> str:
    """Serialize JSON deterministically."""

    return json.dumps(value, ensure_ascii=False, sort_keys=True, separators=(",", ":"), allow_nan=False)


def sha256_bytes(value: bytes) -> str:
    """Hash bytes."""

    return hashlib.sha256(value).hexdigest()


def sha256_file(path: Path) -> str:
    """Hash one file."""

    return hashlib.sha256(path.read_bytes()).hexdigest()


def load_json(path: Path) -> dict[str, Any]:
    """Load one JSON object."""

    try:
        value = json.loads(path.read_text(encoding="utf-8"))
    except (OSError, UnicodeError, json.JSONDecodeError) as exc:
        raise ConfigError(f"cannot load JSON object {path}: {exc}") from exc
    if not isinstance(value, dict):
        raise ConfigError(f"expected JSON object: {path}")
    return value


def load_q040(path: Path) -> dict[str, Any]:
    """Load only q040 without exposing evaluator truth."""

    try:
        rows = [json.loads(line) for line in path.read_text(encoding="utf-8").splitlines() if line.strip()]
    except (OSError, UnicodeError, json.JSONDecodeError) as exc:
        raise ConfigError(f"cannot load hard questions: {exc}") from exc
    row = next((item for item in rows if isinstance(item, dict) and item.get("id") == "q040"), None)
    if not isinstance(row, dict) or set(row) != {"id", "question", "question_type", "qrels"}:
        raise ConfigError("q040 is missing or malformed")
    return {"id": row["id"], "question": row["question"], "question_type": row["question_type"]}


def tree_sha256(root: Path) -> str:
    """Bind one skill package without generated bytecode."""

    rows = [
        {"path": path.relative_to(root).as_posix(), "sha256": sha256_file(path)}
        for path in sorted(item for item in root.rglob("*") if item.is_file() and "__pycache__" not in item.parts)
    ]
    return sha256_bytes(canonical_json(rows).encode("utf-8"))


def response_contract_assertion() -> str:
    """Return the common abstention-safe JSON response contract."""

    return """try {
  const value = JSON.parse(output.trim());
  if (JSON.stringify(Object.keys(value)) !== JSON.stringify(['question_id','answer','evidence'])) return false;
  if (value.question_id !== 'q040' || !Array.isArray(value.evidence)) return false;
  if (value.answer === null) return value.evidence.length === 0;
  if (JSON.stringify(Object.keys(value.answer)) !== JSON.stringify(['summary','claims'])) return false;
  if (typeof value.answer.summary !== 'string' || !value.answer.summary.trim() || !Array.isArray(value.answer.claims)) return false;
  if (!value.answer.claims.every((row) => JSON.stringify(Object.keys(row)) === JSON.stringify(['statement','evidence_indices']) && typeof row.statement === 'string' && row.statement.trim() && Array.isArray(row.evidence_indices) && row.evidence_indices.every(Number.isInteger))) return false;
  return value.evidence.every((row) => JSON.stringify(Object.keys(row)) === JSON.stringify(['source_id','record_id','concept_path','source_path','record_sha256','locator','text_sha256']) && typeof row.source_id === 'string' && typeof row.record_id === 'string' && typeof row.concept_path === 'string' && typeof row.source_path === 'string' && /^[0-9a-f]{64}$/.test(row.record_sha256) && row.locator && typeof row.locator === 'object' && /^[0-9a-f]{64}$/.test(row.text_sha256));
} catch { return false; }"""


def grounded_assertion() -> str:
    """Require a nonempty grounded answer as a separate metric."""

    return """try {
  const value = JSON.parse(output.trim());
  if (value.answer === null || !Array.isArray(value.evidence) || value.evidence.length === 0) return false;
  if (!Array.isArray(value.answer.claims) || value.answer.claims.length === 0) return false;
  return value.answer.claims.every((claim) => claim.evidence_indices.length > 0 && claim.evidence_indices.every((index) => index >= 0 && index < value.evidence.length));
} catch { return false; }"""


def prompt(question: str) -> str:
    """Render one non-leaking, identical prompt for every control/treatment cell."""

    return f"""Answer the Astro technical question below using only the published Semantic OKF snapshot at `knowledge/`. Do not use the web, model memory, or guesses. The snapshot is immutable; do not write into it. Use the available consultation skill if one is installed. If the snapshot cannot support an answer, return `answer: null` and an empty `evidence` array.

Question: {question}

Return JSON only with top-level keys `question_id`, `answer`, and `evidence`, in that order. Set `question_id` to `q040`. A non-null `answer` must contain `summary` and `claims`, in that order. Give a precise 160-320 word synthesis that covers output mode, adapter, route prerendering, endpoint behavior, request-time cookies, and the inverse server-default design. Each claim must contain exactly `statement` and `evidence_indices`; indices are zero-based references into `evidence`. Every evidence row must contain exactly `source_id`, `record_id`, `concept_path`, `source_path`, `record_sha256`, `locator`, and `text_sha256`, copied from an exact validated consultation hit. Keep evidence in first-use order. Do not include benchmark-only relevance labels or unsupported facts."""


