def atomic_write(path: Path, content: str) -> None:
    """Publish one generated config or manifest atomically."""

    path.parent.mkdir(parents=True, exist_ok=True)
    temporary = path.with_name(f".{path.name}.{secrets.token_hex(5)}.tmp")
    try:
        temporary.write_text(content, encoding="utf-8", newline="\n")
        os.replace(temporary, path)
    finally:
        temporary.unlink(missing_ok=True)


def generate(args: argparse.Namespace) -> dict[str, Any]:
    """Generate and bind every paired config."""

    build = load_json(args.build_report)
    if build.get("status") != "pass" or build.get("authoritative_core_parity") is not True:
        raise ConfigError("build report is not an accepted all-family parity run")
    run_dir = build.get("run_dir")
    if not isinstance(run_dir, str) or not (REPO / run_dir).is_dir():
        raise ConfigError("build report run_dir is unavailable")
    question = load_q040(args.questions)
    coverage = prompt_coverage()
    coverage_path = args.output / "prompt-coverage.json"
    atomic_write(
        coverage_path,
        json.dumps(coverage, ensure_ascii=False, sort_keys=True, indent=2) + "\n",
    )
    outputs = []
    for family in FAMILIES:
        payload = yaml.dump(
            config(family, run_dir, question),
            Dumper=Dumper,
            sort_keys=False,
            allow_unicode=True,
            default_flow_style=False,
            width=1000,
        )
        path = args.output / f"q040-{family}-paired.yaml"
        atomic_write(path, payload)
        skill = REPO / "skills" / SKILLS[family]
        outputs.append(
            {
                "family": family,
                "config": path.relative_to(REPO).as_posix(),
                "config_sha256": sha256_file(path),
                "skill_id": SKILLS[family],
                "skill_tree_sha256": tree_sha256(skill),
                "bundle": f"{run_dir}/bundles/{family}-a",
            }
        )
    manifest = {
        "schema_version": SCHEMA,
        "status": "pass",
        "interpretation": "Each file is an isolated same-bundle knowledge-only control versus one-skill treatment. The six files are separate paired diagnostics, never an all-skills causal portfolio.",
        "question": question,
        "question_source": {
            "path": args.questions.relative_to(REPO).as_posix(),
            "sha256": sha256_file(args.questions),
        },
        "prompt_coverage": {
            "path": coverage_path.relative_to(REPO).as_posix(),
            "sha256": sha256_file(coverage_path),
            "scope": "one-question boundary diagnostic, not an aggregate capability estimate",
        },
        "build_report": {
            "path": args.build_report.relative_to(REPO).as_posix(),
            "sha256": sha256_file(args.build_report),
            "run_dir": run_dir,
        },
        "model": "openai-codex/gpt-5.6-luna",
        "requests_per_cell": 1,
        "comparisons": outputs,
    }
    atomic_write(args.output / "q040-config-manifest.json", json.dumps(manifest, ensure_ascii=False, sort_keys=True, indent=2) + "\n")
    return manifest


def parse_args(argv: Sequence[str] | None = None) -> argparse.Namespace:
    """Parse generator arguments."""

    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--build-report", type=Path, default=EVALUATION / "reports/build-comparison.json")
    parser.add_argument("--questions", type=Path, default=EVALUATION / "benchmark/hard-questions.jsonl")
    parser.add_argument("--output", type=Path, default=OUTPUT)
    args = parser.parse_args(argv)
    for name in ("build_report", "questions", "output"):
        setattr(args, name, getattr(args, name).resolve())
    return args


def main(argv: Sequence[str] | None = None) -> int:
    """Generate the accepted configs and manifest."""

    args = parse_args(argv)
    try:
        report = generate(args)
    except (ConfigError, OSError, UnicodeError, ValueError, TypeError, KeyError) as exc:
        print(json.dumps({"status": "error", "error": str(exc)}, ensure_ascii=False), file=sys.stderr)
        return 1
    print(json.dumps({"status": "pass", "configs": len(report["comparisons"]), "output": str(args.output)}))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
